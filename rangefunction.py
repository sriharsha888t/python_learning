# range function operations
print(list(range(10)))
print(list(range(0,10,5)))
print(list(range(0,10)))
print(list(range(0,100,10)))
print(list(range(0,100,20)))
print(list(range(0,-10,-2)))
print(list(range(-100,-1000,-100)))
print(set(range(0,10)))
print(tuple(range(10,100)))
print(list(range(1,100,2+2)))
print(list(range(1,100,2**3)))
print(set(range(0,101,25)))